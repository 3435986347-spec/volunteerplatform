-- V11 volunteer 加「管理团队」标记
-- 需求方澄清：管理团队本质就是给志愿者多一个字段标记——被标记者参加活动按「管理团队积分倍率」(默认 ×1.2) 计分，
--   补上之前「积分倍率 ×1.2 没有数据通道」的缺口（现场负责人 ×1.4 走 activity_leader，普通 ×1.0，管理团队 ×1.2 走本标记）。
-- 「报名管理团队」(问卷式申请：单选/多选/判断/简答/文件上传/日期选择 + 批量下载) 与
-- 「活动临时负责人考试·资格」(考试达分获资格、评价过低经组织部审核取消、历史考试板块、主观题人工审核) 均为
--   预留功能（归 organization 域），本期不建——届时由其审批/考试结果回写本标记或独立资格表。

ALTER TABLE volunteer
    ADD COLUMN manager_flag TINYINT NOT NULL DEFAULT 0
        COMMENT '管理团队标记 0否/1是（参加活动积分按管理团队倍率；报名管理团队/审批为预留功能）';
